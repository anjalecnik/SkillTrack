function generateSimpleHash(id: number): number {
  let hash = 0;
  hash = (hash << 5) - hash + id.toString().charCodeAt(0) * id;
  return Math.abs(hash);
}

export function generateColor(id: number): string {
  const index = generateSimpleHash(id) % ColorList.length;
  return ColorList[index];
}
